# Table 2 — provenance_inventory
# ---------------------------------------------------------------------------

def write_provenance_inventory(out_dir: Path) -> None:
    """Join node coverage with layer metadata for a paper-facing provenance table."""
    cov_path = results_root() / "tables" / "table_node_coverage.csv"
    if not cov_path.exists():
        logger.warning("table_node_coverage.csv not found; skipping provenance inventory.")
        return

    cov = pl.read_csv(cov_path).filter(pl.col("node_id") != "__event_panel__")

    # Build layer + primary-features lookup per event/node
    layer_rows: list[dict] = []
    for event_id in _EVENTS:
        for node in nodes_for_event(event_id):
            layer_rows.append({
                "event_id":    event_id,
                "node_id":     node.id,
                "layer":       getattr(node, "layer", ""),
            })
    if not layer_rows:
        logger.warning("No node metadata; provenance inventory will lack layer column.")
        layer_df = pl.DataFrame({"event_id": [], "node_id": [], "layer": []})
    else:
        layer_df = pl.DataFrame(layer_rows)

    # Derive claim ceiling from tier
    def _claim_ceiling(tier: str) -> str:
        mapping = {
            "A":                    "A/A DEX-flow, A/A settlement, A/B, B/B",
            "B":                    "A/B suggestive, B/B context",
            "C":                    "context / taxonomy only",
            "fixture_non_empirical": "not claimable",
            "missing":              "not claimable",
        }
        return mapping.get(str(tier), "unknown")

    tier_col = "source_tier_actual" if "source_tier_actual" in cov.columns else "tier_actual"
    cov = cov.join(layer_df, on=["event_id", "node_id"], how="left")
    cov = cov.with_columns(
        (pl.col(tier_col).map_elements(_claim_ceiling, return_dtype=pl.String))
        .alias("claim_ceiling"),
        (pl.col(tier_col).is_in(["fixture_non_empirical"]))
        .alias("fixture_flag"),
    )

    keep = [c for c in [
        "event_id", "node_id", "layer", tier_col,
        "sources", "coverage_pct", "rows_available",
        "fixture_flag", "claim_ceiling",
    ] if c in cov.columns]
    out = out_dir / "table_provenance_inventory.csv"
    cov.select(keep).write_csv(out)
    logger.info("Wrote %s (%d rows)", out.name, cov.height)


