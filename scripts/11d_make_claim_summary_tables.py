# Table 1 — feature_tiers
# ---------------------------------------------------------------------------

def write_feature_tiers(out_dir: Path) -> None:
    """Flatten configs/feature_tiers.yaml to a CSV."""
    from stressnet.config import _CONFIGS_DIR
    yaml_path = _CONFIGS_DIR / "feature_tiers.yaml"
    if not yaml_path.exists():
        logger.warning("feature_tiers.yaml not found; skipping.")
        return
    with yaml_path.open() as fh:
        raw = yaml.safe_load(fh) or {}
    features = raw.get("features", {})
    rows = []
    for feat, meta in features.items():
        rows.append({
            "feature_col":               feat,
            "tier":                      str(meta.get("tier", "B")),
            "evidence_type":             str(meta.get("evidence_type", "")),
            "claim_language":            str(meta.get("claim_language", "")),
            "effective_tier_without_l2": str(meta.get("effective_tier_without_l2", "")),
            "notes":                     str(meta.get("notes", "")).strip(),
        })
    if rows:
        df = pl.DataFrame(rows).sort("tier")
        out = out_dir / "table_feature_tiers.csv"
        df.write_csv(out)
        logger.info("Wrote %s (%d features)", out.name, df.height)


