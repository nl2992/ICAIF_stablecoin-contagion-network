"""Build focused paper-summary tables from claim-gated result files.

Outputs written to results/paper/tables/:

  table_feature_tiers.csv          Feature-level provenance tiers (from YAML).
  table_provenance_inventory.csv   Node provenance, coverage, and claim ceiling.
  table_aa_provenance_valid_edges.csv  All A/A provenance-valid edges.
  table_aa_paper_claimable_edges.csv   A/A edges that pass both provenance + statistical gates.
  table_ab_suggestive_edges.csv    A/B edges that pass the paper gate.
  table_claim_audit_summary.csv    Per-event claim-gate counts (Table 6 / audit figure).

Usage:
    python scripts/11d_make_claim_summary_tables.py
    python scripts/11d_make_claim_summary_tables.py --paper-dir results/paper/tables
"""

from __future__ import annotations

import argparse
from pathlib import Path

import polars as pl
import yaml

from stressnet.config import results_root
from stressnet.graph.nodes import nodes_for_event
from stressnet.utils.logging import get_logger

logger = get_logger(__name__)

_EVENTS = ["usdc_svb_2023", "terra_luna_2022", "usdt_curve_2023", "ftx_2022", "busd_2023"]
_AA_LEVELS = frozenset({
    "A_A_dex_flow",
    "A_A_onchain_settlement",
    "A_A_cex_microstructure",
    "A_A_high_provenance",
})
_BOOL_COLS = (
    "provenance_claim_allowed",
    "statistical_claim_allowed",
    "paper_claim_allowed",
    "uses_fixture",
)

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

_SKIP_PREFIXES = (
    # Claim-gate audits (summaries, not edge tables)
    "table_claim_gate",
    "table_claim_language",
    # Already-consolidated summary tables generated by 99_make_paper_outputs.py
    # or this script — reading them causes duplicate edges.
    "table_aa_amm",
    "table_aa_provenance",
    "table_aa_paper",
    "table_ab_suggestive",
    "table_provenance_claimable",
    "table_statistically_supported",
    "table_claim_audit",
    # Non-edge paper outputs
    "table_paper_summary",
    "table_node_coverage",
    "table_provenance_inventory",
    "table_feature_tiers",
    "table_event_study",
    "table_node_centrality",
    "table_prediction",
)


def _load_paper_tables(paper_dir: Path) -> pl.DataFrame | None:
    """Concatenate per-event claim-bearing result tables into one frame.

    Reads from results/paper/tables/ but skips consolidated summary tables
    to avoid double-counting the same edge from multiple aggregated files.
    Only per-event primary result files (leadlag, TE, granger, tvp_var*) are
    included so each edge row is counted exactly once.
    """
    # Read from the annotated results/tables/ dir so we get each edge once.
    src_dir = paper_dir.parent.parent / "tables"
    if not src_dir.exists():
        src_dir = paper_dir  # fallback

    frames: list[pl.DataFrame] = []
    for p in sorted(src_dir.glob("*.csv")):
        if any(p.name.startswith(pfx) for pfx in _SKIP_PREFIXES):
            continue
        if not any(p.name.startswith(pfx) for pfx in (
            "table_leadlag_tests_",
            "table_transfer_entropy_",
            "table_granger_",
            "table_tvp_var_summary_",
            "table_var_spillovers_",
            "table_sparse_events_",
        )):
            continue
        try:
            df = pl.read_csv(p)
        except Exception:
            continue
        if "claim_level" not in df.columns:
            continue
        df = df.with_columns(pl.lit(p.name).alias("source_table"))
        frames.append(df)

    if not frames:
        return None
    # Cast all columns to String before concat to avoid type-mismatch across tables.
    str_frames = [f.select([pl.col(c).cast(pl.String) for c in f.columns]) for f in frames]
    return pl.concat(str_frames, how="diagonal")


def _parse_bools(df: pl.DataFrame) -> pl.DataFrame:
    """Cast boolean-string columns to Polars Boolean."""
    for col in _BOOL_COLS:
        if col in df.columns and df[col].dtype != pl.Boolean:
            df = df.with_columns(
                (pl.col(col).cast(pl.String).str.to_lowercase().is_in(["true", "1", "yes"]))
                .alias(col)
            )
    return df


