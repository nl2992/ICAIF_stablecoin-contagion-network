# Tables 3-5 — A/A valid, A/A paper, A/B suggestive
# ---------------------------------------------------------------------------

def write_aa_ab_tables(df: pl.DataFrame, out_dir: Path) -> None:
    """Write A/A provenance-valid, A/A paper-claimable, and A/B paper-claimable tables."""
    df = _parse_bools(df)

    prov_col  = "provenance_claim_allowed"
    paper_col = "paper_claim_allowed"

    # Guard: ensure columns exist
    if prov_col not in df.columns:
        df = df.with_columns(pl.lit(False).alias(prov_col))
    if paper_col not in df.columns:
        df = df.with_columns(pl.lit(False).alias(paper_col))

    # Resolve node_i / node_j regardless of column naming convention
    def _resolve_edge_cols(frame: pl.DataFrame) -> pl.DataFrame:
        """Normalise to node_i / node_j; drop self-loops."""
        if "node_i" not in frame.columns or "node_j" not in frame.columns:
            for src, tgt in [("causing_node", "caused_node"),
                             ("source_node_id", "target_node_id"),
                             ("source_node", "target_node"),
                             ("source", "target")]:
                if src in frame.columns and tgt in frame.columns:
                    frame = frame.rename({src: "node_i", tgt: "node_j"})
                    break
        # Drop self-loops (VAR/FEVD diagonal; not cross-node propagation)
        if "node_i" in frame.columns and "node_j" in frame.columns:
            frame = frame.filter(pl.col("node_i") != pl.col("node_j"))
        return frame

    df = _resolve_edge_cols(df)

    # Table 3: A/A provenance-valid (all A/A levels, provenance gate passes)
    aa_prov = df.filter(
        pl.col("claim_level").is_in(list(_AA_LEVELS))
        & pl.col(prov_col)
    )
    if aa_prov.height > 0:
        aa_prov.write_csv(out_dir / "table_aa_provenance_valid_edges.csv")
        logger.info(
            "Wrote table_aa_provenance_valid_edges.csv (%d rows, claim levels: %s)",
            aa_prov.height,
            aa_prov["claim_level"].unique().sort().to_list(),
        )
        print("\n=== A/A provenance-valid edges ===")
        print(aa_prov.group_by("claim_level").len().sort("len", descending=True))
    else:
        logger.warning("No A/A provenance-valid edges found.")

    # Table 4: A/A paper-claimable (both gates pass)
    aa_paper = df.filter(
        pl.col("claim_level").is_in(list(_AA_LEVELS))
        & pl.col(paper_col)
    )
    if aa_paper.height > 0:
        aa_paper.write_csv(out_dir / "table_aa_paper_claimable_edges.csv")
        logger.info(
            "Wrote table_aa_paper_claimable_edges.csv (%d rows) — headline A/A claims",
            aa_paper.height,
        )
        print("\n=== A/A paper-claimable edges (headline results) ===")
        display_cols = [c for c in [
            "event_id", "node_i", "node_j", "feature_col", "source_table",
            "claim_level", "claim_strength", "paper_claim_allowed",
        ] if c in aa_paper.columns]
        print(aa_paper.select(display_cols))
    else:
        logger.warning(
            "No A/A paper-claimable edges. "
            "High-provenance AMM flow is available as descriptive evidence "
            "(see table_aa_provenance_valid_edges.csv), but none pass the statistical gate. "
            "Use claim_strength='suggestive' language for these pairs."
        )

    # Table 5: A/B paper-claimable
    ab_paper = df.filter(
        (pl.col("claim_level") == "A_B_suggestive_directional")
        & pl.col(paper_col)
    )
    if ab_paper.height > 0:
        ab_paper.write_csv(out_dir / "table_ab_suggestive_edges.csv")
        logger.info(
            "Wrote table_ab_suggestive_edges.csv (%d rows) — A/B suggestive propagation",
            ab_paper.height,
        )
    else:
        logger.warning("No A/B paper-claimable edges found.")


# ---------------------------------------------------------------------------
# Table 6 — claim_audit_summary
# ---------------------------------------------------------------------------

def write_claim_audit(df: pl.DataFrame, out_dir: Path) -> None:
    """Write per-event claim-gate audit counts (Table 6, anti-cherry-pick evidence)."""
    df = _parse_bools(df)

    audit_rows: list[dict] = []
    events_in_df = (
        [e for e in df["event_id"].drop_nulls().unique().sort().to_list() if e]
        if "event_id" in df.columns else ["all"]
    )

    for event_id in events_in_df:
        g = df.filter(pl.col("event_id") == event_id) if "event_id" in df.columns else df
        prov_ok  = "provenance_claim_allowed" in g.columns
        stat_ok  = "statistical_claim_allowed" in g.columns
        paper_ok = "paper_claim_allowed" in g.columns
        fix_ok   = "uses_fixture" in g.columns

        audit_rows.append({
            "event_id":              event_id,
            "n_total_edges":         g.height,
            "n_fixture_blocked":     int(g.filter(pl.col("uses_fixture")).height) if fix_ok else 0,
            "n_provenance_claimable": int(g.filter(pl.col("provenance_claim_allowed")).height) if prov_ok else 0,
            "n_statistically_supported": int(g.filter(pl.col("statistical_claim_allowed")).height) if stat_ok else 0,
            "n_paper_claimable":     int(g.filter(pl.col("paper_claim_allowed")).height) if paper_ok else 0,
            "n_AA_provenance":       int(g.filter(
                pl.col("claim_level").is_in(list(_AA_LEVELS))
                & (pl.col("provenance_claim_allowed") if prov_ok else pl.lit(True))
            ).height),
            "n_AA_paper_claimable":  int(g.filter(
                pl.col("claim_level").is_in(list(_AA_LEVELS))
                & (pl.col("paper_claim_allowed") if paper_ok else pl.lit(False))
            ).height),
            "n_AB_paper_claimable":  int(g.filter(
                (pl.col("claim_level") == "A_B_suggestive_directional")
                & (pl.col("paper_claim_allowed") if paper_ok else pl.lit(False))
            ).height),
            "n_BB_context":          int(g.filter(
                pl.col("claim_level") == "B_B_context_only"
            ).height),
        })

    audit = pl.DataFrame(audit_rows)
    out = out_dir / "table_claim_audit_summary.csv"
    audit.write_csv(out)
    logger.info("Wrote %s (%d events)", out.name, audit.height)
    print("\n=== Claim audit summary ===")
    print(audit)


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main() -> None:
    parser = argparse.ArgumentParser(description="Build paper claim-summary tables.")
    parser.add_argument(
        "--paper-dir",
        default=None,
        help="Directory to read/write paper tables (default: results/paper/tables).",
    )
    args = parser.parse_args()

    paper_dir = Path(args.paper_dir) if args.paper_dir else results_root() / "paper" / "tables"
    paper_dir.mkdir(parents=True, exist_ok=True)

    # 1. Feature-tier table (from YAML — no paper tables needed)
    write_feature_tiers(paper_dir)

    # 2. Provenance inventory (from node coverage + configs)
    write_provenance_inventory(paper_dir)

    # 3-6. Load all claim-bearing paper tables
    combined = _load_paper_tables(paper_dir)
    if combined is None:
        logger.error(
            "No claim-bearing paper tables found in %s. "
            "Run 00c_claim_gate.py --all-events --strict first.",
            paper_dir,
        )
        raise SystemExit(1)

    write_aa_ab_tables(combined, paper_dir)
    write_claim_audit(combined, paper_dir)

    logger.info("Claim summary tables complete.")


if __name__ == "__main__":
    main()
